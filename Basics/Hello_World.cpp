// Include the iostream header file for input/output operations
#include <iostream>

// Define the main function, the program's entry point
int main() {
    
    int cout=30;  // Declare a variable and assign 30
    
    // Output "Hello World" to the console followed by a newline
    std::cout << "Hello World" << std::endl;

    std::cout<<cout<<std::endl; // Output the value of variable cout
    
    // Return 0 to indicate successful program execution
    return 0;
}
